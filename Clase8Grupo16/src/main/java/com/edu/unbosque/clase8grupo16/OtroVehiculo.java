public class OtroVehiculo extends Vehiculo{
    
    private int cantMotores;
    private String modelo;

    /**
     * @return the cantMotores
     */
    public int getCantMotores() {
        return cantMotores;
    }

    /**
     * @param cantMotores the cantMotores to set
     */
    public void setCantMotores(int cantMotores) {
        this.cantMotores = cantMotores;
    }

    /**
     * @return the modelo
     */
    public String getModelo() {
        return modelo;
    }

    /**
     * @param modelo the modelo to set
     */
    public void setModelo(String modelo) {
        this.modelo = modelo;
    }
    
    
}
